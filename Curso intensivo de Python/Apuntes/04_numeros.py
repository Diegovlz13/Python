# Números en Python
entero = 10
print(entero)
print(type(entero))

decimal = 10.5
print(decimal)
print(type(decimal)) # Muestra el tipo de dato

complejo = 3 + 4j
print(complejo)
print(type(complejo))

# Convertir un número decimal a entero
entero = int(decimal)
print(entero)
print(type(entero))
# Convertir un número entero a decimal
decimal = float(entero)
print(decimal)
print(type(decimal))